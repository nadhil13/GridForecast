"use client";

import { motion, AnimatePresence } from "framer-motion";
import { TrendingUp } from "lucide-react";

import type { ForecastDataPoint } from "./types";

/* ============================================================
   ResultCard — displays the latest prediction with spring anim
   ============================================================ */

interface ResultCardProps {
  prediction: ForecastDataPoint | null;
}

export function ResultCard({ prediction }: ResultCardProps) {
  return (
    <AnimatePresence mode="wait">
      {prediction && (
        <motion.div
          key={prediction.tahun}
          layout
          initial={{ opacity: 0, scale: 0.96, y: 12 }}
          animate={{ opacity: 1, scale: 1, y: 0 }}
          exit={{ opacity: 0, scale: 0.96, y: -8 }}
          transition={{
            type: "spring",
            stiffness: 100,
            damping: 20,
          }}
          className="rounded-xl border border-border/40 bg-card p-6 shadow-sm"
        >
          <div className="flex items-start justify-between">
            <div>
              <p className="text-xs font-medium text-muted-foreground">
                Estimated Production
              </p>
              <p className="mt-1 text-3xl font-bold tabular-nums tracking-tight">
                {prediction.produksi_twh.toFixed(2)}
                <span className="ml-1.5 text-base font-normal text-muted-foreground">
                  TWh
                </span>
              </p>
            </div>
            <div className="flex h-10 w-10 items-center justify-center rounded-lg bg-secondary text-foreground">
              <TrendingUp className="h-5 w-5" />
            </div>
          </div>
          <p className="mt-3 text-xs text-muted-foreground">
            Forecasted national electricity production for year{" "}
            <span className="font-semibold text-foreground">
              {prediction.tahun}
            </span>
            , generated by the AI prediction model.
          </p>
        </motion.div>
      )}
    </AnimatePresence>
  );
}
